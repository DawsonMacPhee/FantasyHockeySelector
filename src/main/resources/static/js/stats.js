/**
* Add stat panels on right side of landing page
* @param {JSON LIST of statistic leaders} leaderList 
*/
function playerStatTable(leaderList) {
    element = "playerTable";
    statList = ["name", "season", "team", "position", "games", "goals", "assists", "points", "plusMinus", "pim", "shots", "shotPct", "timeOnIcePerGame", "faceOffPct", "blocked", "hits"]; //list of stats
    for (let i in leaderList.players) { //create panel for each stat
        playerImg = "https://cms.nhl.bamgrid.com/images/headshots/current/168x168/" + leaderList.players[i].playerId + "@2x.jpg"; //store player img src
        teamImg = "../assets/logos/logo_" + leaderList.players[i].team + ".svg"; //save player team logo src
        ppg = parseFloat(leaderList.players[i].points) / parseFloat(leaderList.players[i].games);
        document.getElementById(element).innerHTML +=
        `<tr>
            <th style="text-align: left;">${(parseInt(i)+1)}. <img src="${playerImg}" class="image_table"> ${leaderList.players[i].name}</th>
            <td>${leaderList.players[i].season}</td>
            <td> <img src="${teamImg}" class="image_table"> ${leaderList.players[i].team}</td>
            <td>${leaderList.players[i].position}</td>
            <td>${leaderList.players[i].games}</td>
            <td>${leaderList.players[i].goals}</td>
            <td>${leaderList.players[i].assists}</td>
            <td>${leaderList.players[i].points}</td>
            <td>${leaderList.players[i].plusMinus}</td>
            <td>${leaderList.players[i].pim}</td>
            <td>${ppg.toFixed(2)}</td>
            <td>${leaderList.players[i].shots}</td>
            <td>${leaderList.players[i].shotPct}</td>
            <td>${leaderList.players[i].timeOnIcePerGame}</td>
            <td>${leaderList.players[i].faceOffPct}</td>
            <td>${leaderList.players[i].blocked}</td>
            <td>${leaderList.players[i].hits}</td>
        </tr>`;
    }   
}  
        
