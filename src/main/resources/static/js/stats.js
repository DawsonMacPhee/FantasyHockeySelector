function getSearchParameters() {
    let playerPosition = "";
    let statType = "";
    if (document.getElementById("gorp").value.localeCompare("player") == 0) {
        playerPosition = document.getElementById("playerPosition").value;
        statType = document.getElementById("playerStats").value;
    }
    else{
        statType = document.getElementById("goalieStats").value;
    }
    var searchParameters = {};
    searchParameters.season = document.getElementById("season").value;
    searchParameters.seasonType =document.getElementById("type").value;
    searchParameters.playerType = document.getElementById("gorp").value;
    searchParameters.playerPosition = playerPosition;
    searchParameters.statType = statType;
    searchParameters.order = document.getElementById("order").value;
    searchParameters.numberOfResults = document.getElementById("results").value;
    
    return searchParameters
}