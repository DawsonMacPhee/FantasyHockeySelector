function createSeasonSelection() {
    element = "season";
    var date = new Date();
    var year = date.getFullYear();
    if (date.getMonth() >= 9) {
        for (let index = year; index >= year-5; index--) {
            document.getElementById(element).innerHTML +=
            `<option value="${index}${index+1}">${index}-${index+1}</option>`;
        }
    }
    else{
        for (let index = year-1; index >= year-5; index--) {
            document.getElementById(element).innerHTML +=
            `<option value="${index}${index+1}">${index}-${index+1}</option>`;
        }
    }
}

